MAX_DALI = 254

def clamp(x, lo=0.0, hi=100.0):
    return max(lo, min(hi, x))

def get_dali_for_percent(percent):
    percent = clamp(percent)
    for i, p in enumerate(power_lookup):
        if p >= percent:
            return i
    return MAX_DALI

def perc_to_dali(percent):
    if percent >= 100:
        return MAX_DALI
    return get_dali_for_percent(percent)
